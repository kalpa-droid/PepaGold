document.addEventListener("DOMContentLoaded", () => {
    const userLang = navigator.language || navigator.userLanguage;
    let langCode = userLang.split('-')[0].toLowerCase();
    
    // Solo reemplazamos por JS los bloques principales pre-traducidos 
    // y dejamos que la traducción nativa del navegador se encargue del resto
    // protegiendo siempre las marcas (ya envueltas en <span translate="no">).
    if (langCode !== 'es' && translations[langCode]) {
        document.documentElement.lang = langCode;
        
        // Marquee especial
        const t = translations[langCode];
        if (document.getElementById("promo-text")) document.getElementById("promo-text").innerHTML = t.promo_text;
        if (document.getElementById("promo-link")) document.getElementById("promo-link").innerHTML = t.promo_button;
        document.querySelectorAll(".promo-text-copy").forEach(el => el.innerHTML = t.promo_text);
        document.querySelectorAll(".promo-link-copy").forEach(el => el.innerHTML = t.promo_button);

        // Bloques generales
        const elementsToTranslate = document.querySelectorAll("[data-i18n]");
        elementsToTranslate.forEach(el => {
            const key = el.getAttribute("data-i18n");
            if (translations[langCode][key]) {
                el.innerHTML = translations[langCode][key];
            }
        });
    } else if (langCode !== 'es') {
        // Para idiomas como Japonés, Ruso, etc., forzamos el lang="es" para que el navegador 
        // sugiera la traducción automática por defecto usando su motor (ej. Google Translate nativo).
        document.documentElement.lang = "es"; 
    }
});
